'''
Представь, что ты печёшь блины для 100 гостей.

    Список: ты печёшь сразу 100 блинов, ставишь стопкой, потом раздаёшь

    Генератор: ты печёшь по одному блину и сразу отдаёшь гостю"
'''
# Генератор — элементы создаются по одному
def pancake_generator():
    for i in range(100):
        yield f"Блин {i+1}"  # отдаём один блин
        # представь, что тут печём следующий

# Использование
for pancake in pancake_generator():
    print(f"Гость получил: {pancake}")
    if pancake == "Блин 5":  # можем остановиться в любой момент
        print("Все наелись!")
        break