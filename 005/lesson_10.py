# Что происходит, когда путаешь порядок?

def create_user(name, age, is_admin):
    print(f"Создан пользователь: {name}, возраст: {age}")
    if is_admin:
        print("Права администратора!")

# ВАРИАНТ 1: Правильный порядок
create_user("Анна", 25, False)  # Анна, 25 лет, не админ

# ВАРИАНТ 2: Перепутанный порядок (катастрофа!)
create_user(False, "Анна", 25)  
# Выведет: "Создан пользователь: False, возраст: Анна"
# А затем попробует проверить is_admin = 25 (что всегда True!)

# ВАРИАНТ 3: С именованными аргументами ошибка невозможна
create_user(age=25, is_admin=False, name="Анна")  # Всё правильно, несмотря на порядок