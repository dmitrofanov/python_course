"""
Дан список транзакций: каждая транзакция содержит категорию и сумму.
Выведи:
1. Общую сумму по каждой категории
2. Категорию с максимальной суммой
3. Категорию с минимальной суммой
"""

transactions = [
    ("еда", 500),
    ("транспорт", 300),
    ("еда", 200),
    ("кафе", 1000),
    ("транспорт", 150),
    ("еда", 400),
    ("кафе", 500),
    ("развлечения", 800),
]

total = {}
for category, amount in transactions:
    total[category] = total.get(category,0) + amount
print(total)

print(min(total))

