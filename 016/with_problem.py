# Плохой код — забыли закрыть файл!
file = open("data.txt", "w")
file.write("Hello, World!")
# Забыли file.close() — файл остался открытым

# Что плохого?










# Проблемы предыдущего кода:
# Утечка памяти
# Файл может быть заблокирован для других программ
# Данные могут не записаться (буферизация)

# Хороший код, но громоздкий
file = open("data.txt", "w")
try:
    file.write("Hello, World!")
finally:
    file.close()  # Закроется даже при ошибке
print("Файл обработан")






# Идеальный код — коротко и надёжно
with open("data.txt", "w") as file:
    file.write("Hello, World!")
print("Файл автоматически закрыт")

# with сам вызывает close() при выходе из блока
# Даже если будет ошибка — файл закроется
# Код чище и понятнее